    public void LoadData(){
        String[] columnName={"Mã ncc","Tên ncc","Địa chỉ","SDT","Email","image"};
        Vector vctHeader = new Vector();
        Vector vctData = new Vector();
        vctHeader.add("Mã ncc");
        vctHeader.add("Tên ncc");
        vctHeader.add("Địa chỉ");
        vctHeader.add("SDT");
        vctHeader.add("Email");
        vctHeader.add("image");
        List<QlyNCC> list = nccDao.getAll();
        list.stream().map((t) -> {
            Vector vector = new Vector();
            vector.add(t.getMancc());
            vector.add(t.getTenncc());
            vector.add(t.getDiachi());
            vector.add(t.getSdt());
            vector.add(t.getEmail());
            vector.add(t.getImage());
            return vector;
        }).forEachOrdered((t) -> {
            vctData.add(t);
        });
        tableModel = new DefaultTableModel(vctData,vctHeader);
        tableNcc.setModel(tableModel);
    }
