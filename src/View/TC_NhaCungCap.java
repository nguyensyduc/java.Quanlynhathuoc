    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        String maNCC = txtMa.getText();
        String tenNCC =txtTen.getText();
        String diaChi = txtDiachi.getText();
        String sdt = txtSdt.getText();
        String email = txtEmail.getText();
        String image = selectImagePath ;
        if (tenNCC!=null && !tenNCC.equals("")) {
            List<QlyNCC> listCheck = nccDao.getid(txtMa.getText());
            if(listCheck.size()>0){
                JOptionPane.showMessageDialog(null, "Mã nhà cung cấp bị trùng", "vui lòng kiểm tra lại", JOptionPane.ERROR_MESSAGE);
                txtMa.requestFocus();
            }
            else{
               if(nccDao.insert(new QlyNCC(maNCC, tenNCC, diaChi, sdt, email, image))>0) {
                    JOptionPane.showMessageDialog(null, "Thêm "+txtTen.getText()+" thành công !");
                    SetControls();
                    LoadData();
                }
                else{
                    JOptionPane.showMessageDialog(null, "Thêm nhà cung cấp thất bại !");
                } 
            }
            
        }
        else{
            JOptionPane.showMessageDialog(null, "Bạn phải nhập tên Nhà cung cấp!!!!");
        }
    }//GEN-LAST:event_btnThemActionPerformed

