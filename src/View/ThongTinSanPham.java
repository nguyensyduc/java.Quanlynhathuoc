        txtMaSP.setText((String)(tableModel.getValueAt(index,0)));
        txtTenSP.setText((String)(tableModel.getValueAt(index,1)));
        txtGia.setText((String.valueOf(tableModel.getValueAt(index,2))));
        txtDonvi.setText((String)(tableModel.getValueAt(index,3)));
        txtSoluong.setText((String.valueOf(tableModel.getValueAt(index,4))));
        txtNgaynhap.setText((String)(tableModel.getValueAt(index,5)));
        txtNgayhet.setText((String)(tableModel.getValueAt(index,6)));
        String path = tableModel.getValueAt(index,7).toString();
        ImageIcon ii = new ImageIcon(path);
        Image image = ii.getImage().getScaledInstance(fileLabel2.getWidth(), fileLabel2.getHeight(), Image.SCALE_SMOOTH);
        fileLabel2.setIcon(new ImageIcon(image));
        txtNcc.setText((String)(tableModel.getValueAt(index,8)));
