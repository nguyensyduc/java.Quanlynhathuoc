    public void LoadData(){
        String[] columnName={"Mã sp","Tên sp","Giá","Đơn vị","Số lượng","Ngày nhập","Ngày hết hạn","image","Nhà cung cấp"};
        Object[] rowData;
        tableModel.setColumnIdentifiers(columnName);
        List<NhapSP> list = spDao.getAll();
        for (NhapSP c : list) {
            rowData = new Object[]{c.getMasp(),c.getTensp(),c.getGia(),c.getDonvi(),c.getSoLuong(),c.getNgaynhap(),c.getNgayhethan(),c.getLoadImage(),c.getNcc()};
            tableModel.addRow(rowData);
        }
       tableSP.setModel(tableModel);
    }

