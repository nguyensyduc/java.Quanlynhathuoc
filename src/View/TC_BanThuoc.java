    private void loadCboTenNV() {
        cbxNVmodel = new DefaultComboBoxModel();
        List<QlyNV> nv = nvDao.getAll();
        cbxTenNV.removeAll();
        cbxNVmodel.removeAllElements();
        for(QlyNV nhanvien : nv) {
            cbxNVmodel.addElement(nhanvien);
            cbxTenNV.setModel(cbxNVmodel);
            
        }
    }

    private void loadCboTenThuoc() {
        cbxSPmodel = new DefaultComboBoxModel();
        List<NhapSP> thuoc = thuocDao.getAll();
        cbxTenThuoc.removeAll();
        cbxSPmodel.removeAllElements();
        for(NhapSP t : thuoc) {
            cbxSPmodel.addElement(t);
            cbxTenThuoc.setModel(cbxSPmodel);
            
        }
    }
    private void loadTableHeader() {
        hdThuocHeader = new Vector();
        hdThuocHeader.add("ID");
        hdThuocHeader.add("Tên thuốc");
        hdThuocHeader.add("Số lượng");
        hdThuocHeader.add("Đơn giá");
        hdThuocHeader.add("Tổng tiền");
    }
    private void loadTableContent() {
        loadTableHeader();
        hdThuocText = new Vector();

        for (NhapSP thuoc : hdThuoc) {
            Vector vct = new Vector();
            vct.add(thuoc.getMasp());
            vct.add(thuoc.getTensp());
            vct.add(thuoc.getSoLuong());
            vct.add(thuoc.getGia());
            vct.add(thuoc.getGia()*thuoc.getSoLuong());
            hdThuocText.add(vct);
        }

        defaultTableModel = new DefaultTableModel(hdThuocText, hdThuocHeader);
        tblHD.setModel(defaultTableModel);
        tinhThanhTien();
    }
    private void tinhTongTien() {
        try {
            Long soluong = Long.parseLong(txtSoLuong.getText());
            Long dongia = Long.parseLong(txtGia.getText());
            Long tong = soluong*dongia;
            lblTong.setText(tong+"");
        } catch (Exception ex) {
            JOptionPane.showConfirmDialog(null, "Đã có lỗi xảy ra!!!");
            return;
        }
    }
    private void tinhThanhTien() {
        try {
            int total = 0;
            for (NhapSP thuoc : hdThuoc) {
                total += (thuoc.getSoLuong()* thuoc.getGia());
            }
            lblThanhTien.setText(total+"");
        } catch (Exception ex) {
            JOptionPane.showConfirmDialog(null, "Đã có lỗi xảy ra!!!");
            return;
        }
    }
