    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if (!(lblTong.getText()).equals("")) {
            int indexSelected = cbxTenThuoc.getSelectedIndex();
            NhapSP thuoc = (NhapSP) cbxSPmodel.getElementAt(indexSelected);
            HoaDon hoadon = new HoaDon(Integer.parseInt(txtMaHD.getText()), getmanv, txtNgayLap.getText(), Integer.parseInt(lblTong.getText()));
            List<HoaDon> listCheck = hdDao.getcateid(Integer.parseInt(txtMaHD.getText()));
            if(listCheck.size()>0){
                JOptionPane.showMessageDialog(null, "Mã ID bị trùng", "vui lòng kiểm tra lại", JOptionPane.ERROR_MESSAGE);
                txtMaHD.requestFocus();
            }
            else{
                hdThuoc.add(new NhapSP(thuoc.getMasp(), thuoc.getTensp(), Integer.parseInt(txtGia.getText()), thuoc.getDonvi(),
                    Integer.parseInt(txtSoLuong.getText()), thuoc.getNgaynhap(), thuoc.getNgayhethan(), thuoc.getLoadImage(), thuoc.getNcc()));
                loadTableContent();
                JOptionPane.showMessageDialog(null, "Thêm "+thuoc.getTensp()+" vào hóa đơn thành công!");
            }
            
        }else{
            JOptionPane.showMessageDialog(null, "Vui lòng chọn thuốc bạn muốn thêm vào hóa đơn!");
        }
    }//GEN-LAST:event_btnThemActionPerformed

