    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        try {
            String manv = txtMaNV.getText();
            String tennv = txtTenNV.getText();
            String luongCb = txtLuong.getText();
            String ngaysinh = txtNgaySinh.getText();
            String que = txtQue.getText();
            String gioitinh = "";
            int fluongCb = 0;
            try {
                fluongCb=Integer.parseInt(luongCb);
                if(rdoNam.isSelected()){
                    gioitinh = "Nam";
                }
                else{
                    gioitinh = "Nữ";    
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
            QlyNV nv = new QlyNV(manv, tennv, fluongCb, que, ngaysinh, gioitinh);
            int tbao = JOptionPane.showConfirmDialog(rootPane, "Bạn có muốn sửa nhân viên "+txtTenNV.getText() +" không", "!!!!!", JOptionPane.YES_NO_OPTION);
            if(tbao==JOptionPane.YES_OPTION){
                nvDao.update(nv);
                JOptionPane.showMessageDialog(rootPane, "Sửa thành công");
                this.dispose();
                new QuanLyNhanVien().setVisible(true);
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnSuaActionPerformed

